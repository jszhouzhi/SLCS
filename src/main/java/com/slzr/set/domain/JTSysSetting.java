package com.slzr.set.domain;

public class JTSysSetting {

	// 1 //物理卡号, 2 //逻辑卡号
	private String initParam;
	// 1 //反转， 2 //不反转
	private String reverseCardNO;
	// 1 // 密钥方案 1 - N
	private String keyType;
	// 卡号长度(CardNOLength)
	private String cardNOLength;

	public JTSysSetting() {
	}

	public JTSysSetting(String initParam, String reverseCardNO, String keyType, String cardNOLength) {
		super();
		this.initParam = initParam;
		this.reverseCardNO = reverseCardNO;
		this.keyType = keyType;
		this.cardNOLength = cardNOLength;
	}

	public String getInitParam() {
		return initParam;
	}

	public void setInitParam(String initParam) {
		this.initParam = initParam;
	}

	public String getReverseCardNO() {
		return reverseCardNO;
	}

	public void setReverseCardNO(String reverseCardNO) {
		this.reverseCardNO = reverseCardNO;
	}

	public String getKeyType() {
		return keyType;
	}

	public void setKeyType(String keyType) {
		this.keyType = keyType;
	}

	public String getCardNOLength() {
		return cardNOLength;
	}

	public void setCardNOLength(String cardNOLength) {
		this.cardNOLength = cardNOLength;
	}

}
